% --- Executes on button press in pushbutton3.
function pushbutton3_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton3 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

image = handles.I;
statis = handles.statis;

% Warna
    rataR=mean(mean(image(:,:,1)));
    rataG=mean(mean(image(:,:,2)));
    rataB=mean(mean(image(:,:,3)));
    stdR=std(std(double(image(:,:,1))));
    stdG=std(std(double(image(:,:,2))));
    stdB=std(std(double(image(:,:,3))));
    skewnR=skewness(skewness(double(image(:,:,1))));
    skewnG=skewness(skewness(double(image(:,:,2))));
    skewnB=skewness(skewness(double(image(:,:,3)))) ;
    entropR=entropy(double(image(:,:,1)));
    entropG=entropy(double(image(:,:,2)));
    entropB=entropy(double(image(:,:,3)));
    
    % Bentuk
    area = statis.Area;
    perimeter = statis.Perimeter;
    metric = 4*pi*area/(perimeter^2);
    majoraxis = statis.MajorAxisLength;
    minoraxis = statis.MinorAxisLength;
    eccentricity = statis.Eccentricity;
    
    set(handles.edit3,'String',num2str(rataR))
    set(handles.edit4,'String',num2str(rataG))
    set(handles.edit5,'String',num2str(rataB))
    set(handles.edit6,'String',num2str(stdR))
    set(handles.edit7,'String',num2str(stdG))
    set(handles.edit8,'String',num2str(stdB))
    set(handles.edit9,'String',num2str(skewnR))
    set(handles.edit10,'String',num2str(skewnG))
    set(handles.edit11,'String',num2str(skewnB))
    set(handles.edit12,'String',num2str(entropR))
    set(handles.edit13,'String',num2str(entropG))
    set(handles.edit14,'String',num2str(entropB))
    set(handles.edit15,'String',num2str(area))
    set(handles.edit16,'String',num2str(perimeter))
    set(handles.edit17,'String',num2str(metric))
    set(handles.edit18,'String',num2str(majoraxis))
    set(handles.edit19,'String',num2str(minoraxis))
    set(handles.edit20,'String',num2str(eccentricity))
    
    input = [area;perimeter;metric;majoraxis;minoraxis;eccentricity; rataR;rataG;rataB;stdR;stdG;stdB;skewnR;skewnG;skewnB; entropR;entropG;entropB];
    
    % menyimpan variabel input
    handles.input = input;
    guidata(hObject, handles)
    

